#include "3-calc.h"


/**
 * op_add - adds two number
 * @a: first number
 * @b: second number
 *
 * Return: return int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two number
 * @a: first number
 * @b: second number
 *
 * Return: return int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two number
 * @a: first number
 * @b: second number
 *
 * Return: return int
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divides two number
 * @a: first number
 * @b: second number
 *
 * Return: return int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modules two number
 * @a: first number
 * @b: second number
 *
 * Return: return int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
